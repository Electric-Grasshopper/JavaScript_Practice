function changeText() {
    // Function to change heading text...
    var heading_1 = document.getElementById("heading_1");
    heading_1.innerHTML = "The Text is changed";
}

function resetText() {
    // Function to reset heading text...
    var heading_1 = document.getElementById("heading_1");
    heading_1.innerHTML = "This is a heading.";
}

function redText() {
    // Function to make heading text red...
    var heading_1 = document.getElementById("heading_1");
    heading_1.style.color = "red"
}

function greenText() {
    // Function to make heading text green...
    var heading_1 = document.getElementById("heading_1");
    heading_1.style.color = "green"
}

